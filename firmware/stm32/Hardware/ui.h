#ifndef __UI_H
#define __UI_H

#include <stdint.h>

/*
 * ui —— 电阻触摸屏多页面工作界面（HX8357DN 320x480 竖屏）
 *
 * 页面：
 *   0 主界面     系统/模式/舵机/继电器/box/温湿度/风速/命令/报警总览
 *   1 手动控制   舵机1-4 加减/预设档位/归位（含确认）
 *   2 自动模式   自动/手动切换、舵机1自动旋转 暂停/恢复
 *   3 设备控制   继电器、box、servo5/6 位置与到位进度（含确认）
 *   4 传感器     温湿度/风速/风向 + 阈值 + 状态灯 + 最值
 *   5 报警/日志  最近命令/错误/计数/运行时长/最近动作
 *   6 设置       波特率/初始角/限位/报警阈值/背光/触摸校准/恢复默认
 *
 * 所有控制动作统一经 ProcessJSONCommand() 下发，复用既有校验（模式锁、限位等）。
 */

// 初始化触摸屏（含首次校准）并绘制首个页面
void UI_Init(void);

// 每个主循环调用一次（约 50ms）：轮询触摸并分发到当前页
void UI_Poll(void);

// 每个主循环调用一次：按需渲染当前页（全量/动态刷新自动判断）
void UI_Render(void);

#endif
